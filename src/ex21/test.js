const { solution } = require('./index');

describe('ex21. 창고 재고 이동 시뮬레이션', () => {
  // ── 기본 케이스 ─────────────────────────────────────────────────────────────
  // 손추적: W1[1:20] W2[2:10,1:10] W3[2:20] W4[4:30,3:30] → 출처 1 제거
  test('기본 예제: 쪼개기 + 통째 이동 + 출처 1 제거 → [0, 10, 20, 60]', () => {
    expect(solution(
      [30, 30, 30, 30],
      [[1, 2, 10], [2, 3, 20], [3, 4, 30]],
      [1]
    )).toEqual([0, 10, 20, 60]);
  });

  // 손추적: 3→1 이동 12가 [3:10] 통째 + [1:5]에서 2만 쪼개 두 덩어리에 걸침
  // W1[1:5,3:10,1:2]=17, W2[2:5]=5, W3[1:3,2:5]=8 (총량 30 보존)
  test('여러 덩어리에 걸친 이동, 제거 없음 → [17, 5, 8]', () => {
    expect(solution(
      [10, 10, 10],
      [[1, 3, 5], [2, 3, 5], [3, 1, 12]],
      []
    )).toEqual([17, 5, 8]);
  });

  // 손추적: 위와 동일 배치에서 출처 1 제거 → W1[3:10], W2[2:5], W3[2:5]
  test('여러 창고에 흩어진 출처 1 전부 제거 → [10, 5, 5]', () => {
    expect(solution(
      [10, 10, 10],
      [[1, 3, 5], [2, 3, 5], [3, 1, 12]],
      [1]
    )).toEqual([10, 5, 5]);
  });

  // 손추적: W2[2:20,1:10]에서 15를 빼면 FIFO라 맨 앞 [2:20]에서 15가 나감
  // W1[2:15], W2[2:5,1:10] → 출처 2 제거 → [0, 10] (LIFO면 [10, 0]이 나와 구분됨)
  test('FIFO 순서 검증: 되돌아오는 이동 → [0, 10]', () => {
    expect(solution(
      [10, 20],
      [[1, 2, 10], [2, 1, 15]],
      [2]
    )).toEqual([0, 10]);
  });

  // 손추적: 출처 1 재고가 1→2→3으로 두 번 이동해도 라벨 유지
  // W2[1:4], W3[3:6,2:6,1:2] → 출처 2 제거 → [0, 4, 8]
  test('두 번 이동한 재고도 출처 유지 → [0, 4, 8]', () => {
    expect(solution(
      [6, 6, 6],
      [[1, 2, 6], [2, 3, 8]],
      [2]
    )).toEqual([0, 4, 8]);
  });

  // 손추적: W2에 모인 세 덩어리 [2:5,1:5,3:5]를 15로 전부 통째 이동
  // W3[2:5,1:5,3:5] → 출처 2 제거 → [0, 0, 10]
  test('창고 전체 비우기: 여러 덩어리 통째 이동 → [0, 0, 10]', () => {
    expect(solution(
      [5, 5, 5],
      [[1, 2, 5], [3, 2, 5], [2, 3, 15]],
      [2]
    )).toEqual([0, 0, 10]);
  });

  // ── 엣지 케이스 ─────────────────────────────────────────────────────────────
  // 이동 없이 복수 출처 제거
  test('이동 없음 + 복수 출처 제거 → [0, 5, 0]', () => {
    expect(solution([5, 5, 5], [], [1, 3])).toEqual([0, 5, 0]);
  });

  // removes에 같은 출처가 중복으로 들어와도 동일하게 동작
  test('removes 중복 원소 → [0, 8]', () => {
    expect(solution([8, 8], [[1, 2, 3]], [1, 1])).toEqual([0, 8]);
  });

  // 모든 출처 제거 → 전부 0
  test('모든 출처 제거 → [0, 0]', () => {
    expect(solution([3, 4], [[2, 1, 2]], [1, 2])).toEqual([0, 0]);
  });

  // 창고 1개: 이동/제거 없음 → 그대로
  test('단일 창고, 이동/제거 없음 → [7]', () => {
    expect(solution([7], [], [])).toEqual([7]);
  });
});
