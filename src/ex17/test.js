const { solution } = require('./index');

describe('ex17. 체육복', () => {
  // ── 기본 케이스 ─────────────────────────────────────────────────────────────
  test('n=5, lost=[2,4], reserve=[1,3,5] → 5', () => {
    expect(solution(5, [2, 4], [1, 3, 5])).toBe(5);
  });

  test('n=5, lost=[2,4], reserve=[3] → 4', () => {
    expect(solution(5, [2, 4], [3])).toBe(4);
  });

  test('n=3, lost=[3], reserve=[1] → 2 (인접하지 않아 빌려줄 수 없음)', () => {
    expect(solution(3, [3], [1])).toBe(2);
  });

  // ── 여벌+도난 동시 보유 케이스 ──────────────────────────────────────────────
  test('여벌 학생이 도난도 당한 경우: n=5, lost=[2,4], reserve=[2,4] → 5', () => {
    expect(solution(5, [2, 4], [2, 4])).toBe(5);
  });

  test('여벌 학생이 도난도 당한 경우: n=3, lost=[1,2], reserve=[2,3] → 2', () => {
    expect(solution(3, [1, 2], [2, 3])).toBe(2);
  });

  // ── 엣지 케이스 ─────────────────────────────────────────────────────────────
  test('도난 학생 없음 → n 반환', () => {
    expect(solution(5, [], [3])).toBe(5);
  });

  test('빌려줄 수 없는 경우: n=2, lost=[1,2], reserve=[] → 0', () => {
    expect(solution(2, [1, 2], [])).toBe(0);
  });
});