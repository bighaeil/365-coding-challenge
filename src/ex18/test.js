const { solution } = require('./index');

describe('ex18. 연속된 자연수의 합', () => {
  // ── 기본 케이스 ─────────────────────────────────────────────────────────────
  // 15 = 1+2+3+4+5 = 4+5+6 = 7+8  → 3가지
  test('n=15 → 3', () => {
    expect(solution(15)).toBe(3);
  });

  // 3 = 1+2  → 1가지
  test('n=3 → 1', () => {
    expect(solution(3)).toBe(1);
  });

  // 1은 연속된 자연수 2개 이상으로 만들 수 없음 → 0
  test('n=1 → 0', () => {
    expect(solution(1)).toBe(0);
  });

  // ── 추가 케이스 ─────────────────────────────────────────────────────────────
  // 10 = 1+2+3+4 = 4+6(X, 비연속) → 1+2+3+4=10 단 하나
  // 손추적: [1,4]=10 ✓, [2,4]=9, [3,5]=12... → 1가지
  test('n=10 → 1', () => {
    expect(solution(10)).toBe(1);
  });

  // 9 = 2+3+4 = 4+5  → 2가지
  // 손추적: [2,4]=9 ✓, [4,5]=9 ✓ → 2가지
  test('n=9 → 2', () => {
    expect(solution(9)).toBe(2);
  });

  // 2는 연속된 자연수 2개 이상으로 만들 수 없음 (1+2=3 이미 초과) → 0
  test('n=2 → 0', () => {
    expect(solution(2)).toBe(0);
  });
});